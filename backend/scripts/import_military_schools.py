def import_data():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    now = datetime.utcnow().isoformat() + "Z"

    # ── Step 1: Upsert majors ──
    major_id_by_code: dict[str, int] = {}
    new_majors = 0
    for code, name, category, discipline, degree_type in MAJORS:
        cur.execute("SELECT id FROM majors WHERE code=?", (code,))
        row = cur.fetchone()
        if row:
            major_id_by_code[code] = row[0]
        else:
            cur.execute(
                "INSERT INTO majors (code, name, category, discipline, degree_type) VALUES (?,?,?,?,?)",
                (code, name, category, discipline, degree_type),
            )
            major_id_by_code[code] = cur.lastrowid
            new_majors += 1
    print(f"Majors: {new_majors} new, {len(MAJORS)-new_majors} existing")

    # ── Step 2: Insert school_major links ──
    inserted = 0
    skipped = 0
    for (school_name, major_code, department, study_mode,
         exam_politics, exam_english, exam_math,
         c1_name, c1_code, c2_name, c2_code, c3_name, c3_code) in SCHOOL_MAJOR_LINKS:

        school_id = SCHOOLS[school_name]
        major_id = major_id_by_code.get(major_code)
        if not major_id:
            print(f"  SKIP: major {major_code} not found for {school_name}")
            skipped += 1
            continue

        # Check if link already exists
        cur.execute(
            "SELECT id FROM school_majors WHERE school_id=? AND major_id=?",
            (school_id, major_id),
        )
        if cur.fetchone():
            skipped += 1
            continue

        cur.execute(
            """INSERT INTO school_majors
               (school_id, major_id, department, direction, study_mode,
                exam_politics, exam_english, exam_math,
                exam_course1_name, exam_course1_code,
                exam_course2_name, exam_course2_code,
                exam_course3_name, exam_course3_code,
                year, data_source, created_at)
               VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)""",
            (
                school_id, major_id, department, "不区分研究方向", study_mode,
                exam_politics, exam_english, exam_math,
                c1_name, c1_code,
                c2_name, c2_code,
                c3_name, c3_code,
                2026, "manual-import", now,
            ),
        )
        inserted += 1

    print(f"School-Major links: {inserted} inserted, {skipped} skipped")
    conn.commit()

    # ── Verify ──
    print("\n=== Verification ===")
    for school_name, sid in SCHOOLS.items():
        cur.execute("SELECT COUNT(*) FROM school_majors WHERE school_id=?", (sid,))
        count = cur.fetchone()[0]
        print(f"  {school_name}: {count} school_majors")

    print(f"\n  Total majors in DB: {cur.execute('SELECT COUNT(*) FROM majors').fetchone()[0]}")
    print(f"  Total school_majors in DB: {cur.execute('SELECT COUNT(*) FROM school_majors').fetchone()[0]}")

    conn.close()


if __name__ == "__main__":
    import_data()
