def get_school_type(name):
    for kw in ["师范"]:
        if kw in name: return SchoolType.NORMAL
    for kw in ["农业","农林","林业","海洋"]:
        if kw in name: return SchoolType.AGRICULTURE
    for kw in ["财经","工商","商业","对外经济贸易","经济贸易"]:
        if kw in name: return SchoolType.FINANCE_ECONOMICS
    for kw in ["医药","医学","中医","中医药","药学","医科","药科","护理"]:
        if kw in name: return SchoolType.MEDICAL
    for kw in ["政法","法学","政治","警官","公安","警察"]:
        if kw in name: return SchoolType.LANGUAGE_LAW
    for kw in ["艺术","美术","音乐","戏剧","戏曲","电影","舞蹈","传媒"]:
        if kw in name: return SchoolType.ART_SPORTS
    for kw in ["体育"]:
        if kw in name: return SchoolType.ART_SPORTS
    for kw in ["外国语","语言"]:
        if kw in name: return SchoolType.LANGUAGE_LAW
    for kw in ["民族"]:
        if kw in name: return SchoolType.COMPREHENSIVE
    for kw in ["理工","工业","工程","科技","电子科技","邮电","建筑","航空","航天",
               "船舶","铁道","交通","地质","矿业","石油","电力","水利水电","纺织",
               "轻工","化工","测绘","冶金","机电","机械"]:
        if kw in name: return SchoolType.SCIENCE_ENGINEERING
    for kw in ["军事","国防","解放军","海军","空军","陆军","火箭军","战略支援"]:
        if kw in name: return SchoolType.MILITARY
    for kw in ["科学","研究"]:
        if kw in name: return SchoolType.RESEARCH
    return SchoolType.COMPREHENSIVE


def derive_province_city(name):
    if name in SPECIAL:
        return SPECIAL[name]
    for prefix_len in (3, 2):
        if len(name) >= prefix_len:
            prefix = name[:prefix_len]
            if prefix in CITY_PROVINCE:
                return CITY_PROVINCE[prefix]
    return (None, None)


def main():
    db = SessionLocal()
    schools = db.query(School).all()
    backup_path = Path(__file__).parent.parent.parent / "crawler_data" / "_school_names_backup.json"
    backup_map = {}
    if backup_path.exists():
        with open(backup_path, "r", encoding="utf-8") as f:
            for item in json.load(f):
                backup_map[item["name"]] = item

    updated, null_prov = 0, 0
    for s in schools:
        if s.province is not None:
            continue
        bu = backup_map.get(s.name)
        if bu:
            s.province = bu["province"]
            s.city = bu.get("city") or bu["province"]
            st = bu.get("school_type")
            if st:
                try: s.school_type = SchoolType[st]
                except: s.school_type = get_school_type(s.name)
            else:
                s.school_type = get_school_type(s.name)
        else:
            prov, city = derive_province_city(s.name)
            if prov:
                s.province = prov
                s.city = city
            else:
                null_prov += 1
            s.school_type = get_school_type(s.name)
        updated += 1

    db.commit()

    remaining = db.query(School).filter(School.province == None).count()
    print(f"Updated: {updated}, NULL province: {remaining}")

    if remaining > 0:
        for s in db.query(School).filter(School.province == None).all():
            print(f"  MISSING: {s.name}")

    dist = db.execute(sa_select(School.province, func.count()).group_by(School.province).order_by(func.count().desc())).all()
    print(f"\nProvince distribution ({len(dist)} provinces):")
    for prov, cnt in dist[:15]:
        print(f"  {prov}: {cnt}")

    tdist = db.execute(sa_select(School.school_type, func.count()).group_by(School.school_type)).all()
    print(f"\nSchool type distribution:")
    for t, cnt in tdist:
        print(f"  {t.value if t else 'NULL'}: {cnt}")

    db.close()
    print("DONE")


if __name__ == "__main__":
    main()
