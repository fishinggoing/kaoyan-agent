def _get_rating_enum(rating: str) -> DisciplineRatingEnum:
    return DisciplineRatingEnum(rating)


def seed():
    db = SessionLocal()
    try:
        existing = db.execute(select(func.count(DisciplineRating.id))).scalar()
        if existing > 100:
            print(f"Already have {existing} discipline ratings. Skipping.")
            return

        all_schools = {
            s.name: s.id
            for s in db.execute(select(School)).scalars().all()
        }
        print(f"Found {len(all_schools)} schools in DB")

        total = 0
        matched = 0
        for disc_code, schools_data in RATINGS.items():
            disc_name = DISCIPLINE_NAMES.get(disc_code, "")
            for school_name, rating_str in schools_data.items():
                school_id = all_schools.get(school_name)
                if school_id:
                    matched += 1
                db.add(DisciplineRating(
                    discipline_code=disc_code,
                    discipline_name=disc_name,
                    school_name=school_name,
                    school_id=school_id,
                    rating=DisciplineRatingEnum(rating_str),
                ))
                total += 1

        db.commit()
        print(f"OK: Seeded {total} discipline ratings ({matched} matched to school_id)")
    except Exception as e:
        db.rollback()
        raise
    finally:
        db.close()


if __name__ == "__main__":
    seed()
