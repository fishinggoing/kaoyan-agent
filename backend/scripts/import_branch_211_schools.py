def import_data():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()
    now = datetime.now(timezone.utc).isoformat().replace("+00:00", "Z")

    # ── Step 1: Upsert majors ──
    major_id_by_code: dict[str, int] = {}
    new_majors = 0
    for code, name, category, discipline, degree_type in NEW_MAJORS:
        cur.execute("SELECT id FROM majors WHERE code=?", (code,))
        row = cur.fetchone()
        if row:
            major_id_by_code[code] = row[0]
        else:
            cur.execute(
                "INSERT INTO majors (code, name, category, discipline, degree_type) VALUES (?,?,?,?,?)",
                (code, name, category, discipline, degree_type),
            )
            major_id_by_code[code] = cur.lastrowid
            new_majors += 1

    # Also load existing codes we reference
    all_codes = set(row[1] for row in SCHOOL_MAJOR_LINKS)
    for code in all_codes:
        if code not in major_id_by_code:
            cur.execute("SELECT id FROM majors WHERE code=?", (code,))
            row = cur.fetchone()
            if row:
                major_id_by_code[code] = row[0]

    print(f"Majors: {new_majors} new, {len(major_id_by_code)} total referenced")

    # ── Step 2: Insert school_major links ──
    inserted = 0
    skipped = 0
    for (school_key, major_code, department, study_mode,
         exam_politics, exam_english, exam_math,
         c1_name, c1_code, c2_name, c2_code, c3_name, c3_code) in SCHOOL_MAJOR_LINKS:

        school_id = SCHOOL_MAP[school_key]
        major_id = major_id_by_code.get(major_code)
        if not major_id:
            print(f"  SKIP: major {major_code} not found for {school_key}")
            skipped += 1
            continue

        cur.execute(
            "SELECT id FROM school_majors WHERE school_id=? AND major_id=? AND department=?",
            (school_id, major_id, department),
        )
        if cur.fetchone():
            skipped += 1
            continue

        cur.execute(
            """INSERT INTO school_majors
               (school_id, major_id, department, direction, study_mode,
                exam_politics, exam_english, exam_math,
                exam_course1_name, exam_course1_code,
                exam_course2_name, exam_course2_code,
                exam_course3_name, exam_course3_code,
                year, data_source, created_at)
               VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)""",
            (
                school_id, major_id, department, "不区分研究方向", study_mode,
                exam_politics, exam_english, exam_math,
                c1_name, c1_code if c1_code else "",
                c2_name, c2_code if c2_code else "",
                c3_name, c3_code if c3_code else "",
                2026, "web-search-import", now,
            ),
        )
        inserted += 1

    print(f"School-Major links: {inserted} inserted, {skipped} skipped")
    conn.commit()

    # ── Verify ──
    print("\n=== Verification ===")
    for sname, sid in SCHOOL_MAP.items():
        cur.execute("SELECT COUNT(*) FROM school_majors WHERE school_id=?", (sid,))
        count = cur.fetchone()[0]
        print(f"  {sname}: {count} school_majors")

    cur.execute("SELECT COUNT(*) FROM majors")
    cur.execute("SELECT COUNT(*) FROM school_majors")
    print(f"\n  Total majors: {cur.execute('SELECT COUNT(*) FROM majors').fetchone()[0]}")
    print(f"  Total school_majors: {cur.execute('SELECT COUNT(*) FROM school_majors').fetchone()[0]}")

    conn.close()


if __name__ == "__main__":
    import_data()
