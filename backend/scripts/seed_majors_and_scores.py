def should_assign(category: str, school_type: str) -> bool:
    """Determine if a major category should be assigned to a school type."""
    aff = CATEGORY_AFFINITY.get(school_type, CATEGORY_AFFINITY["综合"])
    prob = aff.get(category, 0.3)
    return random.random() < prob


def generate_score_lines(school_level: str, category: str, year: int) -> tuple[int, int, int, int, int]:
    """Generate realistic score line based on school level, category and year."""
    base = NATIONAL_LINES.get(year, {}).get(category, (273, 37, 37, 56, 56))
    total, pol, eng, biz1, biz2 = base

    level_bonus = {
        "C9": random.randint(50, 80),
        "985": random.randint(35, 60),
        "211": random.randint(20, 45),
        "双一流": random.randint(15, 35),
        "普本": random.randint(5, 20),
    }
    bonus = level_bonus.get(school_level, 0)
    year_adj = random.randint(-5, 5)

    return (
        max(200, total + bonus + year_adj),
        min(100, pol + random.randint(-5, 5)),
        min(100, eng + random.randint(-5, 5)),
        max(0, biz1 + random.randint(-10, 10)),
        max(0, biz2 + random.randint(-10, 10)),
    )


def generate_re_exam_lines(total: int, pol: int, eng: int, biz1: int, biz2: int) -> tuple[int, int, int, int, int]:
    """Generate re-exam score line as 1.05-1.15x the initial exam line."""
    multiplier = random.uniform(1.05, 1.15)
    return (
        int(total * multiplier),
        min(100, int(pol * multiplier)),
        min(100, int(eng * multiplier)),
        int(biz1 * multiplier) if biz1 > 0 else 0,
        int(biz2 * multiplier) if biz2 > 0 else 0,
    )


def seed():
    db = SessionLocal()
    try:
        existing_majors = db.query(Major).count()
        existing_scores = db.query(ScoreLine).count()
        if existing_majors > 100:
            print(f"Already have {existing_majors} majors. Skipping seed.")
            db.close()
            return

        # Get top schools (C9 + 985 + 211)
        schools = list(db.query(School).filter(
            School.level.in_([SchoolLevel.C9, SchoolLevel.NINE_EIGHT_FIVE, SchoolLevel.TWO_ONE_ONE])
        ).all())

        if not schools:
            print("No schools found. Run import_yantu_schools.py first.")
            db.close()
            return

        print(f"Seeding majors for {len(schools)} top schools ({existing_scores} score lines exist)...")

        total_majors = 0
        total_scores = 0

        for i, school in enumerate(schools):
            stype = school.school_type.value if hasattr(school.school_type, 'value') else str(school.school_type)
            slevel = school.level.value if hasattr(school.level, 'value') else str(school.level)

            # Filter majors by school type affinity
            assigned_in_school = set()

            for cat, first_lvl, code, name, subjects in MAJOR_CATALOG:
                if not should_assign(cat, stype):
                    continue

                # Limit majors per first-level discipline
                fl_key = (school.id, first_lvl)
                if fl_key in assigned_in_school and random.random() < 0.5:
                    continue

                exam_json = json.dumps([s.strip() for s in subjects.split(",")], ensure_ascii=False)

                db.add(Major(
                    code=code,
                    name=name,
                    category=cat,
                    first_level=first_lvl,
                    degree_level=DegreeLevel.MASTER,
                    exam_subjects=exam_json,
                    school_id=school.id,
                ))
                total_majors += 1
                assigned_in_school.add(fl_key)

            # Generate score lines (5 years: 2022-2026), tied to actual major codes
            if existing_scores == 0:
                cats_used: dict[str, list[str]] = {}
                for cat, _fl, code, _name, _subj in MAJOR_CATALOG:
                    if should_assign(cat, stype):
                        cats_used.setdefault(cat, []).append(code)

                for year in [2022, 2023, 2024, 2025, 2026]:
                    for cat, codes in cats_used.items():
                        total, pol, eng, biz1, biz2 = generate_score_lines(slevel, cat, year)
                        sample_codes = random.sample(codes, min(3, len(codes)))
                        for code in sample_codes:
                            re_total, re_pol, re_eng, re_biz1, re_biz2 = generate_re_exam_lines(total, pol, eng, biz1, biz2)
                            db.add(ScoreLine(
                                school_id=school.id,
                                major_code=code,
                                year=year,
                                category="学硕",
                                total_score=total,
                                politics_score=pol,
                                english_score=eng,
                                business_score_1=biz1,
                                business_score_2=biz2,
                                is_national_line=(slevel == "普本"),
                                re_exam_total_score=re_total,
                                re_exam_politics_score=re_pol,
                                re_exam_english_score=re_eng,
                                re_exam_business_score_1=re_biz1,
                                re_exam_business_score_2=re_biz2,
                            ))
                            total_scores += 1

            if (i + 1) % 30 == 0:
                db.commit()
                print(f"  {i+1}/{len(schools)} schools done, {total_majors} majors, {total_scores} scores...")

        db.commit()
        print(f"\nDone: {total_majors} majors, {total_scores} score lines for {len(schools)} schools")
        print(f"Total majors in DB: {db.query(Major).count()}")
        print(f"Total score lines in DB: {db.query(ScoreLine).count()}")

    except Exception as e:
        db.rollback()
        print(f"Seed failed: {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    seed()
