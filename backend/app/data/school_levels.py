_ASSOCIATE_UPGRADE_KEYWORDS = [
    "职业技术学院", "高等专科学校", "职业学院", "技师学院",
    "技工学校", "职业技术学校", "中等专业学校",
]

_VOCATIONAL_UNIVERSITY_KEYWORDS = [
    "职业大学",  # 本科层次职业大学 → 考研高校/普本
]


def classify_category(name: str) -> str:
    """Classify a school by name patterns.

    Returns SAEnum member names: ADULT_EDU / ASSOCIATE_UPGRADE / GRAD_EXAM.
    """
    for kw in _ADULT_EDU_KEYWORDS:
        if kw in name:
            return "ADULT_EDU"
    is_vocational_uni = any(kw in name for kw in _VOCATIONAL_UNIVERSITY_KEYWORDS)
    if not is_vocational_uni:
        for kw in _ASSOCIATE_UPGRADE_KEYWORDS:
            if kw in name:
                return "ASSOCIATE_UPGRADE"
    return "GRAD_EXAM"


def classify_level(name: str, code: str = "") -> str:
    """Classify a 考研高校 by tier: C9 > 985 > 211 > 双一流 > 普本.

    NAME-FIRST matching for accuracy. Code is used as fallback.
    Returns SAEnum member names (C9, NINE_EIGHT_FIVE, TWO_ONE_ONE,
    DOUBLE_FIRST_CLASS, REGULAR).
    """
    # ── Name-first: C9 ──
    if name in C9_NAMES:
        return "C9"

    # ── Name-first: 985 ──
    if name in NAMES_985:
        return "NINE_EIGHT_FIVE"

    # ── Name-first: 211 (with alias resolution) ──
    resolved = _211_NAME_ALIASES.get(name, name)
    if resolved in NAMES_211 or name in NAMES_211:
        return "TWO_ONE_ONE"

    # ── Name-first: 双一流 ──
    if name in NAMES_DOUBLE_FIRST_CLASS:
        return "DOUBLE_FIRST_CLASS"

    # ── Code-based fallback ──
    if code:
        if code in C9_CODES:
            return "C9"
        if code in CODES_985:
            return "NINE_EIGHT_FIVE"
        if code in CODES_211:
            return "TWO_ONE_ONE"
        if code in CODES_DOUBLE_FIRST_CLASS:
            return "DOUBLE_FIRST_CLASS"

    return "REGULAR"
