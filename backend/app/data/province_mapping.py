def get_province(univ_code: str, school_name: str = "", yantu_code: str = "") -> str:
    """Determine the province for a university.

    Priority:
    1. yantu_code positions 2-3 — standard GB province code (most reliable)
    2. SCHOOL_TO_PROVINCE explicit lookup by name
    3. NAME_PROVINCE_HINTS keyword match in school name
    4. univ_code prefix via CODE_TO_PROVINCE
    """
    # 1. yantu code province (positions 2-3 are the standard GB province code)
    if yantu_code and len(yantu_code) >= 4:
        prov_code = yantu_code[2:4]
        province = CODE_TO_PROVINCE.get(prov_code)
        if province:
            return province

    # 2-3. Name-based detection
    if school_name:
        if school_name in SCHOOL_TO_PROVINCE:
            return SCHOOL_TO_PROVINCE[school_name]

        for prov in sorted(NAME_PROVINCE_HINTS, key=lambda x: -len(x)):
            if prov in school_name:
                return NAME_PROVINCE_HINTS[prov]

    # 4. Standard university code prefix fallback
    prefix = univ_code[:2] if univ_code else ""
    return CODE_TO_PROVINCE.get(prefix, "未知")


